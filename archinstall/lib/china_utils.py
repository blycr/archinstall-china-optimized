def setup_china_specific_config(target: Path) -> bool:
	"""
	设置中国用户特定的系统配置

	Args:
		target: 目标系统路径

	Returns:
		是否成功
	"""
	success = True

	try:
		# 1. 配置 pacman
		if not configure_pacman_for_china(target):
			success = False

		# 2. 配置 locale
		locale_gen = target / 'etc/locale.gen'
		if locale_gen.exists():
			content = locale_gen.read_text()
			# 确保 zh_CN.UTF-8 被启用
			if '#zh_CN.UTF-8' in content:
				content = content.replace('#zh_CN.UTF-8', 'zh_CN.UTF-8')
				locale_gen.write_text(content)
				debug('已启用 zh_CN.UTF-8 locale')

		return success

	except Exception as e:
		warn(f'设置中国特定配置失败: {e}')
		return False


