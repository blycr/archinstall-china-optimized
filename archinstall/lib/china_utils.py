def configure_pacman_for_china(target: Path) -> bool:
	"""
	配置 pacman 以在中国网络环境下更好工作

	Args:
		target: 目标系统路径

	Returns:
		是否成功
	"""
	try:
		pacman_conf = target / 'etc/pacman.conf'

		if not pacman_conf.exists():
			warn(f'pacman.conf 不存在: {pacman_conf}')
			return False

		content = pacman_conf.read_text()

		# 优化配置
		modifications = []

		# 1. 启用 ParallelDownloads（如果未启用）
		if 'ParallelDownloads' not in content:
			content = content.replace(
				'#[parallel]',
				'ParallelDownloads = 5\n#[parallel]'
			)
			modifications.append('启用 ParallelDownloads = 5')

		# 2. 启用 Color（如果未启用）
		if '#Color' in content:
			content = content.replace('#Color', 'Color')
			modifications.append('启用 Color')
		elif 'Color' not in content:
			content = 'Color\n' + content
			modifications.append('启用 Color')

		# 3. 启用 ILoveCandy（有趣的进度条）
		if 'ILoveCandy' not in content:
			content = content.replace(
				'[options]',
				'[options]\nILoveCandy'
			)
			modifications.append('启用 ILoveCandy')

		pacman_conf.write_text(content)

		if modifications:
			debug(f'pacman.conf 已优化: {", ".join(modifications)}')

		return True

	except Exception as e:
		warn(f'配置 pacman.conf 失败: {e}')
		return False


def generate_mirrolist_config(mirrors: list[str]) -> str:
	"""
	生成 mirrorlist 配置文件内容

	Args:
		mirrors: 镜像源 URL 列表

	Returns:
		mirrorlist 配置内容
	"""
	lines = [
		'# Arch Linux mirrorlist for China',
		'# Generated by archinstall china optimization',
		'',
	]

	for mirror in mirrors:
		lines.append(f'Server = {mirror}$repo/os/$arch')

	return '\n'.join(lines) + '\n'


def validate_chinese_username(username: str) -> tuple[bool, str]:
	"""
	验证用户名是否符合中国用户习惯

	Args:
		username: 用户名

	Returns:
		(是否有效, 错误信息)
	"""
	# Linux 用户名规则
	if not username:
		return False, '用户名不能为空'

	if len(username) < 1 or len(username) > 32:
		return False, '用户名长度必须在 1-32 个字符之间'

	if not re.match(r'^[a-z_][a-z0-9_-]*$', username):
		return False, '用户名只能包含小写字母、数字、下划线和连字符，且必须以字母或下划线开头'

	if username[0].isdigit():
		return False, '用户名不能以数字开头'

	# 检查是否为保留名
	reserved_names = [
		'root', 'admin', 'administrator', 'user', 'test',
		'guest', 'www', 'http', 'nginx', 'mysql',
	]

	if username.lower() in reserved_names:
		return False, f'"{username}" 是系统保留用户名'

	return True, ''


def get_recommended_partition_scheme(disk_size_gb: int) -> dict:
	"""
	获取推荐的分区方案

	Args:
		disk_size_gb: 磁盘大小（GB）

	Returns:
		分区方案字典
	"""
	if disk_size_gb < 30:
		return {
			'recommended': False,
			'message': '磁盘空间不足 30GB，建议至少使用 30GB 以确保系统正常运行',
			'scheme': 'single',
		}

	if disk_size_gb < 60:
		# 小磁盘：简单分区
		return {
			'recommended': True,
			'message': '建议使用简单分区方案（/ 和 swap）',
			'scheme': 'simple',
			'partitions': [
				{'mountpoint': '/', 'size': '100%', 'filesystem': 'ext4'},
				{'mountpoint': '[swap]', 'size': '2G', 'filesystem': 'swap'},
			]
		}

	if disk_size_gb < 256:
		# 中等磁盘：推荐分区
		return {
			'recommended': True,
			'message': '建议使用标准分区方案',
			'scheme': 'standard',
			'partitions': [
				{'mountpoint': '/boot', 'size': '512M', 'filesystem': 'fat32'},
				{'mountpoint': '/', 'size': '50G', 'filesystem': 'ext4'},
				{'mountpoint': '/home', 'size': '100%', 'filesystem': 'ext4'},
				{'mountpoint': '[swap]', 'size': '4G', 'filesystem': 'swap'},
			]
		}

	# 大磁盘：高级分区
	return {
		'recommended': True,
		'message': '磁盘空间充足，可以使用高级分区方案',
		'scheme': 'advanced',
		'partitions': [
			{'mountpoint': '/boot', 'size': '1G', 'filesystem': 'fat32'},
			{'mountpoint': '/', 'size': '100G', 'filesystem': 'ext4'},
			{'mountpoint': '/home', 'size': '100%', 'filesystem': 'ext4'},
			{'mountpoint': '[swap]', 'size': '8G', 'filesystem': 'swap'},
		]
	}


